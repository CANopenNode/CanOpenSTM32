/* USER CODE BEGIN Header_StartCommTask */
/**
* @brief Function implementing the commTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartCommTask */
void StartCommTask(void *argument)
{
  /* USER CODE BEGIN StartCommTask */
  canOpenNodeSTM32.CANHandle = &hcan;
  canOpenNodeSTM32.HWInitFunction = MX_CAN_Init;
  canOpenNodeSTM32.timerHandle = &htim15;
  // make sure node ID is in 1-127 range
  canOpenNodeSTM32.desiredNodeID = 2;
  canOpenNodeSTM32.baudrate = 125;
  int result = canopen_app_init(&canOpenNodeSTM32);
  if (result != 0)
  {
    return;
  }

  /* Infinite loop */
  for (;;)
  {
    canopen_app_process();
    // Sleep for 1ms, you can decrease it if required, in the canopen_app_process we will double check to make sure 1ms
    // passed
    osDelay(pdMS_TO_TICKS(1));
  }
  /* USER CODE END StartCommTask */
}

